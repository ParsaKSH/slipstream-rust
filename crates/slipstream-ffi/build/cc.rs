use std::path::{Path, PathBuf};

/// Compile multiple C source files with a shared include directory and link
/// them as a static library. Uses the `cc` crate for cross-platform support
/// (GCC, Clang, MSVC).
pub(crate) fn compile_cc_lib(name: &str, sources: &[PathBuf], include_dirs: &[&Path]) {
    let mut build = cc::Build::new();
    for source in sources {
        build.file(source);
    }
    for dir in include_dirs {
        build.include(dir);
    }
    build.pic(true);

    // On Windows, picoquic headers use #ifdef _WINDOWS to select Winsock2
    // instead of arpa/inet.h. The cc crate doesn't define these by default.
    let target = std::env::var("TARGET").unwrap_or_default();
    if target.contains("windows") {
        build.define("_WINDOWS", None);
        build.define("WIN32", None);

        // Force-include wincompat.h if it was generated by the picoquic build.
        // This provides ssize_t and winsock types for MSVC.
        if let Some(root) = crate::util::locate_repo_root() {
            let build_dir = std::env::var_os("PICOQUIC_BUILD_DIR")
                .map(std::path::PathBuf::from)
                .unwrap_or_else(|| root.join(".picoquic-build"));
            let wincompat_dir = build_dir.join("wincompat_include");
            let wincompat = wincompat_dir.join("wincompat.h");
            if wincompat.exists() {
                build.flag(format!("/FI{}", wincompat.display()));
                build.include(&wincompat_dir);
            }
        }
    }

    build.compile(name);
}
